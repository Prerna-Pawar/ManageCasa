public class UserProfile extends AppCompatActivity {

    TextView profileName, profileEmail, profileUsername, profilePassword, profileContactNo,profileFlatno;
    TextView titleName, titleUsername;
    Button editProfile;

    @SuppressLint("MissingInflatedId")
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_user_profile);

        profileName = findViewById(R.id.profileName);
        profileContactNo=findViewById(R.id.contact);
        profileFlatno=findViewById(R.id.flat);
        profileEmail = findViewById(R.id.profileEmail);
        profileUsername = findViewById(R.id.profileUsername);
        profilePassword = findViewById(R.id.profilePassword);
        titleName = findViewById(R.id.titleName);
        titleUsername = findViewById(R.id.titleUsername);

        showUserData();


    }

    public void showUserData(){

        Intent intent = getIntent();

        String nameUser = intent.getStringExtra("name");
        String contact=intent.getStringExtra("contactno");
        String flat=intent.getStringExtra("flatno");
        String emailUser = intent.getStringExtra("email");
        String usernameUser = intent.getStringExtra("username");
        String passwordUser = intent.getStringExtra("password");

        titleName.setText(nameUser);
        profileContactNo.setText(contact);
        profileFlatno.setText(flat);
        titleUsername.setText(usernameUser);
        profileName.setText(nameUser);
        profileEmail.setText(emailUser);
        profileUsername.setText(usernameUser);
        profilePassword.setText(passwordUser);
    }



}